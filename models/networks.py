def compute_loss(fusion, img_cat, img_1, img_2, put_type='mean', balance=0.01):
    loss1 = intensity_loss(fusion, img_1, img_2, put_type)
    loss2 = structure_loss(fusion, img_cat)

    return loss1 + balance * loss2


def create_putative(in1, in2, put_type):
    if put_type == 'mean':
        iput = (in1 + in2) / 2
    elif put_type == 'left':
        iput = in1
    elif put_type == 'right':
        iput = in2
    else:
        raise EOFError('No supported type!')

    return iput


def intensity_loss(fusion, img_1, img_2, put_type):
    inp = create_putative(img_1, img_2, put_type)

    # L2 norm
    loss = torch.norm(fusion - inp, 2)

    return loss


def gradient(x):
    H, W = x.shape[2], x.shape[3]

    left = x
    right = F.pad(x, [0, 1, 0, 0])[:, :, :, 1:]
    top = x
    bottom = F.pad(x, [0, 0, 0, 1])[:, :, 1:, :]

    dx, dy = right - left, bottom - top

    dx[:, :, :, -1] = 0
    dy[:, :, -1, :] = 0

    return dx, dy


def create_structure(inputs):
    B, C, H, W = inputs.shape[0], inputs.shape[1], inputs.shape[2], inputs.shape[3]

    dx, dy = gradient(inputs)

    structure = torch.zeros(B, 4, H, W)  # Structure tensor = 2 * 2 matrix

    a_00 = dx.pow(2)
    a_01 = a_10 = dx * dy
    a_11 = dy.pow(2)

    structure[:, 0, :, :] = torch.sum(a_00, dim=1)
    structure[:, 1, :, :] = torch.sum(a_01, dim=1)
    structure[:, 2, :, :] = torch.sum(a_10, dim=1)
    structure[:, 3, :, :] = torch.sum(a_11, dim=1)

    return structure


def structure_loss(fusion, img_cat):
    st_fusion = create_structure(fusion)
    st_input = create_structure(img_cat)

    # Frobenius norm
    loss = torch.norm(st_fusion - st_input)

    return loss


def contrastive_loss(x1, x2, margin=1.0, y=1.0):
    """
    Computes contrastive loss between two 4D tensors (B, C, W, H).

    Args:
        x1: Tensor of shape (B, C, W, H)
        x2: Tensor of shape (B, C, W, H)
        margin: Margin value for contrastive loss

    Returns:
        Contrastive loss value
        :param x1:
        :param x2:
        :param margin:
        :param y:
    """

    # Compute Euclidean distance between the two tensors
    euclidean_distance = torch.norm(x1 - x2, dim=1)

    # Compute contrastive loss using the formula from the paper
    contrastive_loss = torch.mean((1 - y) * torch.pow(euclidean_distance, 2) +
                                  y * torch.pow(torch.clamp(margin - euclidean_distance, min=0.0), 2))
    return contrastive_loss


def psnr_loss(img1, img2):
    mse = torch.mean((img1 - img2) ** 2)
    max_i = img1.max()
    psnr = 10 * torch.log10((max_i ** 2) / mse)
    return psnr


def image_entropy(image):
    # 将图像转换为灰度图，如果是彩色图像，则取RGB通道的平均值
    if len(image.shape) == 3:
        image = torch.mean(image, dim=0)
    # 将图像像素值缩放到[0,1]之间
    image = (image - torch.min(image)) / (torch.max(image) - torch.min(image))
    # 计算像素值的直方图
    histogram = torch.histc(image, bins=256, min=0, max=1)
    # 将像素值的直方图除以总像素数，得到各像素值出现概率分布
    probs = histogram / torch.numel(image)
    # 去除出现概率为0的像素值，避免熵计算出现NaN
    probs = probs[probs > 0]
    # 计算图像熵
    entropy = -torch.sum(probs * torch.log2(probs))
    return entropy.item()


