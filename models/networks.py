class Reconstructor(nn.Module):
    """Define a Reconstructor"""

    def __init__(self, output_nc, ngf, use_bias, norm_layer, edge_net):
        super(Reconstructor, self).__init__()
        self.edge_net = edge_net

        # 上采样
        self.conv_1 = nn.Sequential(nn.ConvTranspose2d(ngf * 4, int(ngf * 2),
                                    kernel_size=3, stride=2,
                                    padding=1, output_padding=1,
                                    bias=use_bias),
                                    norm_layer(int(ngf * 2)),
                                    nn.ReLU(True))

        self.conv_2 = nn.Sequential(nn.ConvTranspose2d(ngf * 2, int(ngf),
                                    kernel_size=3, stride=2,
                                    padding=1, output_padding=1,
                                    bias=use_bias),
                                    norm_layer(int(ngf)),
                                    nn.ReLU(True))

        self.conv_3 = nn.Sequential(nn.ReflectionPad2d(3),
                                    nn.Conv2d(ngf * 2, output_nc, kernel_size=7, padding=0),
                                    nn.Tanh())

    def forward(self, x, input):
        # 边缘特征
        edge_features = self.edge_net(input, 'encoder')  # 1

        conv_1 = self.conv_1(x)
        conv_1_2 = self.conv_1(edge_features[2])  # 128

        conv_2 = self.conv_2(conv_1)
        conv_2_2 = self.conv_2(conv_1_2)  # 64

        # conv_2 = self.conv_2(torch.cat((conv_2, conv_2_2), dim=1))
        # conv_2 = torch.cat((conv_2, edge_features.detach()), dim=1)  # 128
        conv_3 = self.conv_3(torch.cat((conv_2, conv_2_2), dim=1))
        return conv_3


